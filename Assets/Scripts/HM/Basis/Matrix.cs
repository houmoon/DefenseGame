using System;

namespace HM
{
    [Serializable]
    public struct Matrix2X2
    {
        public int x;
        public int y;
    }

    [Serializable]
    public struct Matrix2X2f
    {
        public float x;
        public float y;
    }

    [Serializable]
    public struct Matrix3X3
    {
        public int x;
        public int y;
        public int z;
    }

    [Serializable]
    public struct Matrix3X3f
    {
        public float x;
        public float y;
        public float z;
    }
}

